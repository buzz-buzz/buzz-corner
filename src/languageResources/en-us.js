export function leftDays(start_time, end_time, now_time) {
    let d = TimeHelper.dateDiff(start_time, now_time);

    if (d > 1) {
        return `After ${d} days`;
    }

    if (d === 1) {
        return 'Start tomorrow';
    }

    if (d === 0) {
        return (start_time - now_time > 0 ? 'Start today' : (end_time - now_time > 0 ? 'Started' : 'Ended'))
    }
}
